package com.automation.Practice;

public class squart {
    public static void main(String[] args) {
        int n=10;

        int k=(int)Math.sqrt(n);

        if (n==(k*k))
            System.out.println("Squart");
        else             System.out.println("Not Squart");

        System.out.println(k);
    }
}
